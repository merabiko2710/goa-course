# 2) calculate the sum of numbers from 1 to 10 inclusive.
# ახლა ჩვენ უნდა გავაკეთოთ კოდი რომელიც გაიგებს 10 ის ჯამს.
#  ამისთვის ჩვენ უნდა გავაკეთოთ კოდი რომელითაც გაკეთდება 2 ცვლადი და კოდის პროცესში პირველ ცვლადს მიემატოს მე - 2 ცვლადი და მეორე ცვლადს ნელნელა
# ემატებოდეს 1 იანები.
# ამის დახმარებით კი ტერმინალში (სადაც გამოდის ნამუშევარის შედეგები) დაწერს 10 ის ჯამს.
number = 1
sum_numbers = 0
while number <= 10:
    sum_numbers += number
    number += 1
    print (sum_numbers)
    